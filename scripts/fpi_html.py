@dataclass
class ColumnSpec:
    idx: int
    kind: Literal["quarter", "instant"]
    year: int
    quarter: int
    period_end: str
    label: str
    ytd_months: int = 3


@dataclass
class ParsedTable:
    rows: list[list[str]]
    columns: list[ColumnSpec]
    scale: float = 1.0
    currency: str = "USD"


def sec_get_html(url: str) -> str:
    last_err: Exception | None = None
    for attempt in range(3):
        try:
            time.sleep(0.11)
            req = urllib.request.Request(url, headers={"User-Agent": sec_user_agent()})
            with urllib.request.urlopen(req, timeout=120) as resp:
                return resp.read().decode("utf-8", errors="replace")
        except OSError as e:
            last_err = e
            time.sleep(2.0 * (attempt + 1))
    raise last_err  # type: ignore[misc]


def filing_index(cik: str, accession: str) -> dict[str, Any]:
    acc = accession.replace("-", "")
    return sec_get(f"https://www.sec.gov/Archives/edgar/data/{int(cik)}/{acc}/index.json")


def filing_doc_url(cik: str, accession: str, doc: str) -> str:
    acc = accession.replace("-", "")
    return f"https://www.sec.gov/Archives/edgar/data/{int(cik)}/{acc}/{doc}"


def clean_cell(text: str) -> str:
    text = html.unescape(re.sub(r"<[^>]+>", " ", text))
    return " ".join(text.split())


def parse_number(raw: str) -> float | None:
    if not raw or raw.strip() in ("—", "-", "", "N/A", "n/a"):
        return None
    neg = raw.strip().startswith("(") and raw.strip().endswith(")")
    s = raw.replace(",", "").replace("$", "").replace("€", "").replace("%", "").strip("()").strip()
    if not s or s in ("—", "-"):
        return None
    try:
        v = float(s)
    except ValueError:
        return None
    return -v if neg else v


def normalize_label(label: str) -> str:
    return re.sub(r"\s+", " ", label.strip().lower()).rstrip(":")


def parse_q_header(label: str) -> tuple[int, int] | None:
    m = re.match(r"Q([1-4])\s+(\d{4})", label.strip(), re.I)
    if not m:
        return None
    return int(m.group(2)), int(m.group(1))


def parse_date_header(label: str) -> str | None:
    label = label.strip()
    m = re.match(r"(\d{1,2})\s+([A-Za-z]{3})\s+(\d{4})", label)
    if m:
        day, mon = int(m.group(1)), MONTHS.get(m.group(2).lower()[:3])
        if mon:
            return f"{int(m.group(3)):04d}-{mon:02d}-{day:02d}"
    m = re.match(r"([A-Za-z]{3,9})\s+(\d{1,2}),?\s+(\d{4})", label, re.I)
    if m:
        mon = MONTHS.get(m.group(1).lower()[:3])
        if mon:
            day = int(m.group(2))
            return f"{int(m.group(3)):04d}-{mon:02d}-{day:02d}"
    return None


def period_end_for_quarter(year: int, quarter: int) -> str:
    month_day = {1: (3, 31), 2: (6, 30), 3: (9, 30), 4: (12, 31)}
    m, d = month_day[quarter]
    return f"{year:04d}-{m:02d}-{d:02d}"


def date_to_period_key(period_end: str, fy_end_month: int) -> PeriodKey:
    y, m, _ = map(int, period_end.split("-"))
    fy = y if m <= fy_end_month else y + 1
    start_month = (fy_end_month % 12) + 1
    elapsed = (m - start_month) if m >= start_month else (12 - start_month) + m
    q = elapsed // 3 + 1
    return fy, q


FPI_FINANCIAL_DOC_RE = re.compile(
    r"(fsx|financial|consolidated|standalone|caq|earnings|quarter)",
    re.I,
)


def list_html_filings(cik: str, limit: int = 25) -> list[dict[str, str]]:
    """Quarterly 6-K filings with financial HTML (fsx / consolidated exhibits)."""
    sub = sec_get(f"https://data.sec.gov/submissions/CIK{int(cik):010d}.json")
    recent = sub["filings"]["recent"]
    by_report: dict[str, tuple[int, dict[str, str]]] = {}
    for i, form in enumerate(recent["form"]):
        if form not in FPI_QUARTERLY_FORMS:
            continue
        pe = recent.get("reportDate", [""])[i] or ""
        if not pe:
            continue
        doc = recent.get("primaryDocument", [""])[i] or ""
        score = 0
        if FPI_FINANCIAL_DOC_RE.search(doc):
            score += 100
        if "fsx" in doc.lower():
            score += 50
        entry = {
            "form": form,
            "filingDate": recent["filingDate"][i],
            "accessionNumber": recent["accessionNumber"][i],
            "reportDate": pe,
            "primaryDocument": doc,
            "url": filing_doc_url(cik, recent["accessionNumber"][i], doc),
        }
        prev = by_report.get(pe)
        if prev is None or score > prev[0]:
            by_report[pe] = (score, entry)
    ranked = sorted(by_report.values(), key=lambda x: x[1]["reportDate"], reverse=True)
    out = [entry for score, entry in ranked if score > 0][:limit]
    if len(out) < limit:
        seen = {f["reportDate"] for f in out}
        for score, entry in ranked:
            if entry["reportDate"] in seen:
                continue
            out.append(entry)
            seen.add(entry["reportDate"])
            if len(out) >= limit:
                break
    return out


def _htm_candidates(index: dict[str, Any], primary: str) -> list[tuple[str, int]]:
    """Rank .htm exhibits by size (larger → more likely full financial report)."""
    ranked: list[tuple[str, int]] = []
    for item in index.get("directory", {}).get("item", []):
        name = item.get("name", "")
        if not name.lower().endswith(".htm"):
            continue
        try:
            size = int(str(item.get("size", "0") or "0"))
        except ValueError:
            size = 0
        ranked.append((name, size))
    ranked.sort(key=lambda x: -x[1])
    return ranked


def locate_financial_docs(index: dict[str, Any], primary: str, min_size: int = 20_000) -> list[str]:
    """Content-neutral: prefer large HTML exhibits (financial reports, not cover pages)."""
    ranked = _htm_candidates(index, primary)
    out: list[str] = []
    for name, size in ranked:
        if size >= min_size or (size == 10**9):
            out.append(name)
    if not out and ranked:
        out.append(ranked[0][0])
    return out


def html_has_financial_tables(html_text: str) -> bool:
    """Quick content check before full parse."""
    low = html_text.lower()
    markers = ("net sales", "net revenue", "total assets", "operating activities", "gross profit")
    return sum(1 for m in markers if m in low) >= 2


def extract_tables(html_text: str) -> list[list[list[str]]]:
    tables: list[list[list[str]]] = []
    for block in re.findall(r"<table[^>]*>(.*?)</table>", html_text, re.I | re.S):
        rows: list[list[str]] = []
        for tr in re.findall(r"<tr[^>]*>(.*?)</tr>", block, re.I | re.S):
            cells = [clean_cell(c) for c in re.findall(r"<t[dh][^>]*>(.*?)</t[dh]>", tr, re.I | re.S)]
            if cells:
                rows.append(cells)
        if rows:
            tables.append(rows)
    return tables


def detect_scale_and_currency(rows: list[list[str]]) -> tuple[float, str]:
    text = " ".join(" ".join(r) for r in rows[:8]).lower()
    currency = "USD"
    for code in ("eur", "dkk", "gbp", "chf", "jpy", "twd", "nt$", "ntd", "new taiwan"):
        if code in text:
            currency = code.upper().replace("NT$", "TWD").replace("NTD", "TWD").replace("NEW TAIWAN", "TWD")
            break
    if "million" in text or " mn" in text:
        return 1.0, currency
    if "thousand" in text:
        return 0.001, currency
    # Infer from magnitude in first numeric data cells
    for row in rows[3:15]:
        for cell in row[1:]:
            v = parse_number(cell)
            if v is not None and abs(v) > 1e8:
                return 1e-6, currency
    return 1.0, currency


def align_amount_column_indices(header_rows: list[list[str]], header_indices: list[int]) -> list[int]:
    """Map date/year header columns to Amount sub-columns (TSM %/Amount pairs)."""
    amount_cols: list[int] = []
    for row in header_rows:
        amount_cols = [i for i, c in enumerate(row) if normalize_label(c) == "amount"]
        if amount_cols:
            break
    if not amount_cols:
        return header_indices
    n = len(header_indices)
    # 3M + 6M layouts interleave Amount columns: use every other Amount for quarterly pairs.
    if n * 2 <= len(amount_cols):
        return [amount_cols[i * 2] if i * 2 < len(amount_cols) else amount_cols[-1] for i in range(n)]
    out: list[int] = []
    for hi in header_indices:
        candidates = [a for a in amount_cols if a >= hi]
        out.append(candidates[0] if candidates else hi)
    return out


def ytd_block_start(header_rows: list[list[str]]) -> int:
    """Column index where 6M/9M/12M YTD blocks begin (exclude from quarterly parse)."""
    start = 10_000
    for row in header_rows:
        for i, cell in enumerate(row):
            low = cell.lower()
            if any(
                p in low
                for p in (
                    "six months ended",
                    "nine months ended",
                    "twelve months ended",
                    "year ended",
                    "years ended",
                )
            ):
                start = min(start, i)
    return start


def dedupe_columns(cols: list[ColumnSpec]) -> list[ColumnSpec]:
    seen: set[str] = set()
    out: list[ColumnSpec] = []
    for c in cols:
        if c.period_end in seen:
            continue
        seen.add(c.period_end)
        out.append(c)
    return out


YTD_MONTH_MAP = {"three": 3, "six": 6, "nine": 9, "twelve": 12}


def parse_period_ended_columns(header_rows: list[list[str]], fy_end_month: int) -> list[ColumnSpec]:
    """Three/Six/Nine Months Ended … / year / Amount sparse layouts."""
    month, day, ytd_months = 12, 31, 3
    for header in header_rows:
        text = " ".join(header).lower()
        m = re.search(
            r"(three|six|nine|twelve)\s+months?\s+ended\s+(\d{1,2})?\s*([a-z]{3,9})\s*(\d{1,2})?",
            text,
            re.I,
        )
        if m:
            ytd_months = YTD_MONTH_MAP.get(m.group(1).lower(), 3)
            mon_name = m.group(3).lower()[:3]
            mon = MONTHS.get(mon_name)
            if mon:
                month = mon
                day = int(m.group(2)) if m.group(2) else (30 if mon in (4, 6, 9, 11) else 31)
    year_cells: list[tuple[int, int]] = []
    for header in header_rows:
        for i, cell in enumerate(header):
            if cell and re.match(r"^\d{4}$", cell.strip()):
                year_cells.append((i, int(cell.strip())))
    if not year_cells:
        return []
    years_only = [y for _, y in year_cells]
    if len(years_only) >= 4 and len(years_only) % 2 == 0:
        half = len(years_only) // 2
        if years_only[:half] == years_only[half:]:
            year_cells = year_cells[:half]
    amount_idxs = align_amount_column_indices(header_rows, [i for i, _ in year_cells])
    cols: list[ColumnSpec] = []
    for (hi, year), ai in zip(year_cells, amount_idxs):
        pe = f"{year:04d}-{month:02d}-{day:02d}"
        pk = date_to_period_key(pe, fy_end_month)
        cols.append(
            ColumnSpec(
                idx=ai,
                kind="quarter",
                year=pk[0],
                quarter=pk[1],
                period_end=pe,
                label=str(year),
                ytd_months=ytd_months,
            )
        )
    return dedupe_columns(cols)


def parse_columns(header_rows: list[list[str]], column_mode: Literal["quarter", "instant"], fy_end_month: int) -> list[ColumnSpec]:
    cols = parse_q_columns(header_rows) if column_mode == "quarter" else parse_instant_columns(header_rows, fy_end_month)
    if cols:
        return dedupe_columns(cols)
    if column_mode == "quarter":
        return parse_period_ended_columns(header_rows, fy_end_month)
    return []


def parse_q_columns(header_rows: list[list[str]]) -> list[ColumnSpec]:
    for header in header_rows:
        cols: list[ColumnSpec] = []
        for i, cell in enumerate(header):
            if not cell:
                continue
            q = parse_q_header(cell)
            if q:
                year, quarter = q
                pe = period_end_for_quarter(year, quarter)
                cols.append(
                    ColumnSpec(
                        idx=i,
                        kind="quarter",
                        year=year,
                        quarter=quarter,
                        period_end=pe,
                        label=cell,
                    )
                )
        if cols:
            return cols
    return []


def parse_instant_columns(header_rows: list[list[str]], fy_end_month: int) -> list[ColumnSpec]:
    date_cells: list[tuple[int, str]] = []
    for header in header_rows:
        for i, cell in enumerate(header):
            if not cell:
                continue
            pe = parse_date_header(cell)
            if pe:
                date_cells.append((i, pe))
    if not date_cells:
        return []
    amount_idxs = align_amount_column_indices(header_rows, [i for i, _ in date_cells])
    cols: list[ColumnSpec] = []
    for (hi, pe), ai in zip(date_cells, amount_idxs):
        year, quarter = date_to_period_key(pe, fy_end_month)
        cols.append(
            ColumnSpec(
                idx=ai,
                kind="instant",
                year=year,
                quarter=quarter,
                period_end=pe,
                label=pe,
            )
        )
    return dedupe_columns(cols)


def table_labels(rows: list[list[str]]) -> list[str]:
    return [normalize_label(r[0]) for r in rows if r and r[0]]


def table_score(rows: list[list[str]], needles: tuple[str, ...]) -> int:
    labels = table_labels(rows)
    text = " ".join(labels)
    return sum(10 for n in needles if n in text)


def _header_text(header_rows: list[list[str]]) -> str:
    return " ".join(" ".join(r) for r in header_rows[:8]).lower()


def quarterly_table_adjustment(header_rows: list[list[str]], cols: list[ColumnSpec]) -> int:
    """Prefer Q1–Q4 / three-month tables over full-year year-vs-year P&L layouts."""
    text = _header_text(header_rows)
    bonus = 0
    if re.search(r"\bq[1-4]\b", text):
        bonus += 50
    if "three months ended" in text:
        bonus += 40
    if any(parse_q_header(c.label) for c in cols):
        bonus += 50
    year_only = [c.label.strip() for c in cols if re.fullmatch(r"\d{4}", c.label.strip())]
    if len(year_only) >= 2 and bonus == 0:
        bonus -= 45
    if any(p in text for p in ("year ended", "years ended", "twelve months ended")) and bonus == 0:
        bonus -= 35
    if re.search(r"profit and loss\s+\d{4}\s+\d{4}", text) and bonus == 0:
        bonus -= 40
    return bonus


def find_best_table(
    tables: list[list[list[str]]],
    needles: tuple[str, ...],
    column_mode: Literal["quarter", "instant"],
    fy_end_month: int,
) -> ParsedTable | None:
    best: ParsedTable | None = None
    best_score = 0
    for rows in tables:
        score = table_score(rows, needles)
        if score <= 0:
            continue
        header_rows = rows[:12]
        cols = parse_columns(header_rows, column_mode, fy_end_month)
        if not cols:
            continue
        score += len(cols)
        if column_mode == "quarter":
            score += quarterly_table_adjustment(header_rows, cols)
        if score > best_score:
            scale, currency = detect_scale_and_currency(rows)
            best = ParsedTable(rows=rows, columns=cols, scale=scale, currency=currency)
            best_score = score
    return best


def alias_key(label: str, aliases: dict[str, str]) -> str | None:
    nl = normalize_label(label)
    if nl in aliases:
        return aliases[nl]
    if "eps (basic" in nl:
        return "eps_b"
    for prefix, key in aliases.items():
        if nl == prefix or nl.startswith(prefix + " "):
            return key
    return None


def extract_metrics(table: ParsedTable, aliases: dict[str, str]) -> dict[str, list[float | None]]:
    out: dict[str, list[float | None]] = {}
    col_count = len(table.columns)

    def cell_value(row: list[str], idx: int) -> float | None:
        for off in (0, 1):
            j = idx + off
            if j >= len(row):
                break
            num = parse_number(row[j])
            if num is not None:
                return round(num * table.scale, 2)
        return None

    for row in table.rows:
        if not row or not row[0]:
            continue
        nl = normalize_label(row[0])
        if nl.startswith("...") or nl.startswith("("):
            continue
        key = alias_key(row[0], aliases)
        if not key or key == "skip":
            continue
        vals: list[float | None] = []
        for col in table.columns:
            vals.append(cell_value(row, col.idx))
        if len(vals) == col_count:
            if key in out:
                if sum(v is not None for v in vals) <= sum(v is not None for v in out[key]):
                    continue
            out[key] = vals
    return out


def period_keys_from_table(table: ParsedTable) -> list[tuple[PeriodKey, ColumnSpec]]:
    return [((col.year, col.quarter), col) for col in table.columns]


def merge_metric(
    store: dict[PeriodKey, dict[str, Any]],
    key: PeriodKey,
    field: str,
    value: float | None,
    source: dict[str, str],
) -> None:
    if value is None:
        return
    entry = store.setdefault(key, {"source": source, "fields": {}})
    if entry["fields"].get(field) is None:
        entry["fields"][field] = value


