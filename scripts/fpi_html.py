def series_from_store(store: dict[PeriodKey, dict[str, Any]], keys: list[PeriodKey], field: str) -> list[float | None]:
    return [store[k]["fields"].get(field) for k in keys]


def build_income_rows_from_store(store: dict[PeriodKey, dict[str, Any]], keys: list[PeriodKey]) -> list[dict[str, Any]]:
    n = len(keys)
    rev = series_from_store(store, keys, "rev")
    gross = series_from_store(store, keys, "gross")
    gm = series_from_store(store, keys, "gm_pct")
    net = series_from_store(store, keys, "net")
    eps_b = series_from_store(store, keys, "eps_b")
    op = series_from_store(store, keys, "op")
    cogs_raw = series_from_store(store, keys, "cogs")
    pretax = series_from_store(store, keys, "pretax")
    tax = series_from_store(store, keys, "tax")

    cogs: list[float | None] = []
    cogs_derived: list[bool] = []
    for i in range(n):
        if cogs_raw[i] is not None:
            cogs.append(cogs_raw[i])
            cogs_derived.append(False)
        elif rev[i] is not None and gross[i] is not None:
            cogs.append(rev[i] - gross[i])
            cogs_derived.append(True)
        else:
            cogs.append(None)
            cogs_derived.append(False)

    for i in range(n):
        if gm[i] is None and rev[i] not in (None, 0) and gross[i] is not None:
            gm[i] = round(gross[i] / rev[i] * 100, 1)

    yoy: list[float | None] = []
    for i, r in enumerate(rev):
        j = i + 4
        if r is None or j >= n or rev[j] is None or rev[j] == 0:
            yoy.append(None)
        else:
            yoy.append((r - rev[j]) / abs(rev[j]) * 100)

    metrics: dict[str, list[float | None]] = {
        "Total Revenues": rev,
        "Total Revenues %Chg": yoy,
        "Cost of Sales": cogs,
        "Gross Profit": gross,
        "Gross Profit Margin": gm,
        "Operating Profit": op,
        "Income Before Provision for Income Taxes": pretax,
        "Provision for Income Taxes": tax,
        "Consolidated Net Income": net,
        "Basic EPS": eps_b,
    }

    optional = {
        "Operating Profit",
        "Income Before Provision for Income Taxes",
        "Provision for Income Taxes",
        "Cost of Sales",
        "Gross Profit",
        "Gross Profit Margin",
        "Basic EPS",
    }
    rows_out: list[dict[str, Any]] = []
    for spec in INCOME_ROWS:
        series = metrics.get(spec.label)
        if series is None:
            continue
        if spec.label in optional and all(v is None for v in series):
            continue
        if spec.unit == "$":
            dflags = {"Cost of Sales": cogs_derived, "Gross Profit": cogs_derived}
            dflags_row = dflags.get(spec.label, [False] * n)
            values = [fmt_dollar(v, derived=dflags_row[i]) if v is not None else "—" for i, v in enumerate(series)]
        elif spec.unit == "%":
            values = [fmt_pct(v) if spec.label.endswith("%Chg") else fmt_margin(v) for v in series]
        elif spec.unit == "eps":
            values = [fmt_eps(v) if v is not None else "—" for v in series]
        else:
            values = [str(v) if v is not None else "—" for v in series]
        rows_out.append(row_from_spec(spec, values))
    return rows_out


def build_bs_rows_from_store(store: dict[PeriodKey, dict[str, Any]], keys: list[PeriodKey]) -> list[dict[str, Any]]:
    n = len(keys)
    cash = series_from_store(store, keys, "cash")
    assets = series_from_store(store, keys, "assets")
    current_assets = series_from_store(store, keys, "current_assets")
    noncurrent_assets = series_from_store(store, keys, "noncurrent_assets")
    liab = series_from_store(store, keys, "liab")
    equity = series_from_store(store, keys, "equity")

    for i in range(n):
        if assets[i] is None and current_assets[i] is not None and noncurrent_assets[i] is not None:
            assets[i] = current_assets[i] + noncurrent_assets[i]
        if assets[i] is None and liab[i] is not None and equity[i] is not None:
            assets[i] = liab[i] + equity[i]

    metrics: dict[str, list[float | None]] = {
        "Total Cash and Cash Equivalents": cash,
        "Total Assets": assets,
        "Total Liabilities": liab,
        "Total Shareholders' Equity": equity,
    }

    rows_out: list[dict[str, Any]] = []
    for spec in BALANCE_SHEET_ROWS:
        if spec.section:
            rows_out.append(section_divider_row(spec, n))
            continue
        series = metrics.get(spec.label)
        if series is None:
            continue
        if all(v is None for v in series):
            continue
        derived = spec.label == "Total Cash and Cash Equivalents" and any(v is not None for v in series)
        values = [fmt_dollar(v, derived=derived) if v is not None else "—" for v in series]
        rows_out.append(row_from_spec(spec, values))
    return rows_out


def build_cf_rows_from_store(store: dict[PeriodKey, dict[str, Any]], keys: list[PeriodKey]) -> list[dict[str, Any]]:
    n = len(keys)
    op_cf = series_from_store(store, keys, "op_cf")
    inv_cf = series_from_store(store, keys, "inv_cf")
    fin_cf = series_from_store(store, keys, "fin_cf")
    net_chg = series_from_store(store, keys, "net_chg")
    capex = series_from_store(store, keys, "capex")

    for i in range(n):
        if net_chg[i] is None and op_cf[i] is not None and inv_cf[i] is not None and fin_cf[i] is not None:
            net_chg[i] = op_cf[i] + inv_cf[i] + fin_cf[i]

    fcf = [
        (op_cf[i] + capex[i]) if op_cf[i] is not None and capex[i] is not None else None
        for i in range(n)
    ]

    metrics: dict[str, list[float | None]] = {
        "Cash from Operating Activities": op_cf,
        "Cash from Investing Activities": inv_cf,
        "Cash from Financing Activities": fin_cf,
        "Net Change in Cash": net_chg,
        "Capital Expenditure": capex,
        "Free Cash Flow": fcf,
    }

    rows_out: list[dict[str, Any]] = []
    for spec in CASH_FLOW_ROWS:
        if spec.section:
            rows_out.append(section_divider_row(spec, n))
            continue
        series = metrics.get(spec.label)
        if series is None:
            continue
        if all(v is None for v in series):
            continue
        values = [fmt_dollar(v, derived=spec.derived) if v is not None else "—" for v in series]
        rows_out.append(row_from_spec(spec, values))
    return rows_out


